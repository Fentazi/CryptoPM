    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (new String(jPasswordField1.getPassword()).equals(new String(jPasswordField2.getPassword()))) {
            CryptoPM.database.cur.site = jTextField1.getText();
            CryptoPM.database.cur.login = jTextField2.getText();
            CryptoPM.database.cur.password = new String(jPasswordField1.getPassword());
            CryptoPM.database.cur.comment = jTextArea1.getText();
            CryptoPM.shwpss.updateTable();
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Пароли не совподают");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

